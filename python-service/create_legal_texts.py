# IPC Important Sections
ipc_text = """
INDIAN PENAL CODE 1860 - IMPORTANT SECTIONS

SECTION 299 - CULPABLE HOMICIDE
Whoever causes death by doing an act with the intention of causing death, or with the 
intention of causing such bodily injury as is likely to cause death, or with the knowledge 
that he is likely by such act to cause death, commits the offence of culpable homicide.

SECTION 300 - MURDER
Culpable homicide is murder if the act by which the death is caused is done with the 
intention of causing death, or with the intention of causing such bodily injury as the 
offender knows to be likely to cause the death of the person to whom the harm is caused.

SECTION 302 - PUNISHMENT FOR MURDER
Whoever commits murder shall be punished with death, or imprisonment for life, 
and shall also be liable to fine.

SECTION 304 - PUNISHMENT FOR CULPABLE HOMICIDE NOT AMOUNTING TO MURDER
Whoever commits culpable homicide not amounting to murder shall be punished with 
imprisonment for life, or imprisonment of either description for a term which may 
extend to ten years, and shall also be liable to fine.

SECTION 304A - CAUSING DEATH BY NEGLIGENCE
Whoever causes the death of any person by doing any rash or negligent act not 
amounting to culpable homicide shall be punished with imprisonment of either 
description for a term which may extend to two years, or with fine, or with both.

SECTION 304B - DOWRY DEATH
Where the death of a woman is caused by any burns or bodily injury or occurs under 
abnormal circumstances within seven years of marriage, and it is shown that she was 
subjected to cruelty or harassment by her husband or his relatives for demand of dowry, 
such death shall be called dowry death. Punishment: minimum 7 years to life imprisonment.

SECTION 306 - ABETMENT OF SUICIDE
If any person commits suicide, whoever abets the commission of such suicide 
shall be punished with imprisonment of either description for a term which may 
extend to ten years, and shall also be liable to fine.

SECTION 307 - ATTEMPT TO MURDER
Whoever does any act with such intention or knowledge, and under such circumstances 
that, if he by that act caused death, he would be guilty of murder, shall be punished 
with imprisonment of either description for a term which may extend to ten years, 
and shall also be liable to fine.

SECTION 354 - ASSAULT OR CRIMINAL FORCE TO WOMAN WITH INTENT TO OUTRAGE HER MODESTY
Whoever assaults or uses criminal force to any woman, intending to outrage or knowing 
it to be likely that he will thereby outrage her modesty, shall be punished with 
imprisonment of either description for a term which may extend to two years, 
or with fine, or with both.

SECTION 375 - RAPE
A man is said to commit rape who has sexual intercourse with a woman under circumstances 
falling under any of the following descriptions: against her will, without her consent, 
with her consent obtained by putting her in fear of death or hurt.

SECTION 376 - PUNISHMENT FOR RAPE
Whoever commits rape shall be punished with rigorous imprisonment of either description 
for a term which shall not be less than seven years but which may extend to imprisonment 
for life, and shall also be liable to fine.

SECTION 378 - THEFT
Whoever, intending to take dishonestly any moveable property out of the possession of 
any person without that person's consent, moves that property in order to such taking, 
is said to commit theft.

SECTION 379 - PUNISHMENT FOR THEFT
Whoever commits theft shall be punished with imprisonment of either description for a 
term which may extend to three years, or with fine, or with both.

SECTION 380 - THEFT IN DWELLING HOUSE
Whoever commits theft in any building, tent or vessel, shall be punished with 
imprisonment of either description for a term which may extend to seven years, 
and shall also be liable to fine.

SECTION 383 - EXTORTION
Whoever intentionally puts any person in fear of any injury to that person, or to any 
other, and thereby dishonestly induces the person so put in fear to deliver to any 
person any property or valuable security commits extortion.

SECTION 390 - ROBBERY
In all robbery there is either theft or extortion. Theft is robbery if the offender 
commits or attempts to commit the theft, and for that end voluntarily causes or 
attempts to cause to any person death or hurt or wrongful restraint.

SECTION 395 - PUNISHMENT FOR DACOITY
Whoever commits dacoity shall be punished with imprisonment for life, or with 
rigorous imprisonment for a term which may extend to ten years, and shall also 
be liable to fine.

SECTION 405 - CRIMINAL BREACH OF TRUST
Whoever, being in any manner entrusted with property, or with any dominion over property, 
dishonestly misappropriates or converts to his own use that property, or dishonestly 
uses or disposes of that property in violation of any direction of law commits criminal 
breach of trust.

SECTION 406 - PUNISHMENT FOR CRIMINAL BREACH OF TRUST
Whoever commits criminal breach of trust shall be punished with imprisonment of either 
description for a term which may extend to three years, or with fine, or with both.

SECTION 415 - CHEATING
Whoever, by deceiving any person, fraudulently or dishonestly induces the person so 
deceived to deliver any property to any person, or to consent that any person shall 
retain any property, or intentionally induces the person so deceived to do or omit to 
do anything which he would not do or omit if he were not so deceived, commits cheating.

SECTION 420 - CHEATING AND DISHONESTLY INDUCING DELIVERY OF PROPERTY
Whoever cheats and thereby dishonestly induces the person deceived to deliver any 
property to any person, or to make, alter or destroy the whole or any part of a 
valuable security, shall be punished with imprisonment of either description for a 
term which may extend to seven years, and shall also be liable to fine.

SECTION 441 - CRIMINAL TRESPASS
Whoever enters into or upon property in the possession of another with intent to 
commit an offence or to intimidate, insult or annoy any person in possession of 
such property, commits criminal trespass.

SECTION 463 - FORGERY
Whoever makes any false document or false electronic record or part of a document 
or electronic record with intent to cause damage or injury, to the public or to any 
person, or to support any claim or title, or to cause any person to part with property, 
commits forgery.

SECTION 468 - FORGERY FOR PURPOSE OF CHEATING
Whoever commits forgery, intending that the document or electronic record forged 
shall be used for the purpose of cheating, shall be punished with imprisonment of 
either description for a term which may extend to seven years, and shall also be 
liable to fine.

SECTION 489A - COUNTERFEITING CURRENCY NOTES OR BANK NOTES
Whoever counterfeits, or knowingly performs any part of the process of counterfeiting, 
any currency note or bank note shall be punished with imprisonment for life, or with 
imprisonment of either description for a term which may extend to ten years, 
and shall also be liable to fine.

SECTION 493 - COHABITATION CAUSED BY A MAN DECEITFULLY INDUCING A BELIEF OF LAWFUL MARRIAGE
Every man who by deceit causes any woman who is not lawfully married to him to believe 
that she is lawfully married to him and to cohabit or have sexual intercourse with him 
in that belief, shall be punished with imprisonment of either description for a term 
which may extend to ten years, and shall also be liable to fine.

SECTION 496 - MARRIAGE CEREMONY FRAUDULENTLY GONE THROUGH WITHOUT LAWFUL MARRIAGE
Whoever, dishonestly or with a fraudulent intention, goes through the ceremony of 
being married, knowing that he is not thereby lawfully married, shall be punished 
with imprisonment of either description for a term which may extend to seven years, 
and shall also be liable to fine.

SECTION 498A - HUSBAND OR RELATIVE OF HUSBAND OF A WOMAN SUBJECTING HER TO CRUELTY
Whoever, being the husband or the relative of the husband of a woman, subjects such 
woman to cruelty shall be punished with imprisonment for a term which may extend to 
three years and shall also be liable to fine. Cruelty means wilful conduct likely to 
drive the woman to suicide or cause grave injury, or harassment for unlawful demands 
of property or valuable security.

SECTION 499 - DEFAMATION
Whoever, by words either spoken or intended to be read, or by signs or by visible 
representations, makes or publishes any imputation concerning any person intending 
to harm, or knowing or having reason to believe that such imputation will harm the 
reputation of such person, is said to defame that person.

SECTION 500 - PUNISHMENT FOR DEFAMATION
Whoever defames another shall be punished with simple imprisonment for a term which 
may extend to two years, or with fine, or with both.

SECTION 503 - CRIMINAL INTIMIDATION
Whoever threatens another with any injury to his person, reputation or property, or 
to the person or reputation of any one in whom that person is interested, with intent 
to cause alarm to that person, or to cause that person to do any act which he is not 
legally bound to do, commits criminal intimidation.

SECTION 506 - PUNISHMENT FOR CRIMINAL INTIMIDATION
Whoever commits the offence of criminal intimidation shall be punished with 
imprisonment of either description for a term which may extend to two years, 
or with fine, or with both.

SECTION 509 - WORD, GESTURE OR ACT INTENDED TO INSULT THE MODESTY OF A WOMAN
Whoever, intending to insult the modesty of any woman, utters any word, makes any 
sound or gesture, or exhibits any object, intending that such word or sound shall be 
heard, or that such gesture or object shall be seen by such woman, shall be punished 
with simple imprisonment for a term which may extend to three years, and also with fine.
"""

# Consumer Protection Act
consumer_text = """
CONSUMER PROTECTION ACT 2019 - KEY PROVISIONS

DEFINITION OF CONSUMER (Section 2(7))
A consumer is any person who buys goods or avails services for personal use and not 
for resale or commercial purpose. Includes online purchases.

CONSUMER RIGHTS (Section 2(9))
1. Right to be protected against marketing of goods and services hazardous to life and property
2. Right to be informed about quality, quantity, potency, purity, standard and price
3. Right to be assured access to goods and services at competitive prices
4. Right to be heard and assured consideration at appropriate forums
5. Right to seek redressal against unfair trade practices
6. Right to consumer education

DEFICIENCY IN SERVICE (Section 2(11))
Any fault, imperfection, shortcoming or inadequacy in the quality, nature and manner 
of performance which is required to be maintained by a person in pursuance of a 
contract or as required under any law for the time being in force.

UNFAIR TRADE PRACTICE (Section 2(47))
A trade practice which, for the purpose of promoting the sale, use or supply of any 
goods or for the provision of any service, adopts any unfair method or unfair or 
deceptive practice including false representation about goods or services.

CONSUMER DISPUTES REDRESSAL COMMISSIONS
District Commission: Claims up to Rs 50 lakhs
State Commission: Claims from Rs 50 lakhs to Rs 2 crores  
National Commission: Claims above Rs 2 crores

COMPLAINT PROCEDURE
Any consumer can file complaint about defective goods, deficient services, unfair 
trade practices, or overcharging. Complaint must be filed within 2 years of cause 
of action. Filing fee starts from Rs 200. Can file online at edaakhil.nic.in.

PENALTIES FOR MANUFACTURERS
If goods cause injury: fine up to Rs 10 lakhs, imprisonment up to 2 years
If goods cause death: fine up to Rs 50 lakhs, imprisonment up to 7 years

PRODUCT LIABILITY (Chapter VI)
Manufacturer, seller and service provider are liable for harm caused by defective 
product or deficient service. No need to prove negligence for product liability claims.

E-COMMERCE RULES 2020
E-commerce entities must display country of origin, provide information about sellers, 
acknowledge complaints within 48 hours, redress within 1 month.

MEDIATION
Consumer disputes can be referred to mediation before or during proceedings. 
Mediation centers established at district and state level.
"""

# RTI Act
rti_text = """
RIGHT TO INFORMATION ACT 2005 - COMPLETE GUIDE

OBJECTIVE
To set out the practical regime of right to information for citizens to secure access 
to information under the control of public authorities.

WHO CAN APPLY
Any citizen of India can file RTI application. Non-citizens cannot file RTI.

WHAT INFORMATION CAN BE SOUGHT
Any information held by or under the control of any public authority including:
- Documents, records, memos, emails, opinions, advice, press releases
- Contracts, reports, papers, samples, models, data
- Information held in any electronic form
- Information relating to any private body which can be accessed by the public authority

EXEMPTIONS FROM DISCLOSURE (Section 8)
Information cannot be disclosed if it:
- Affects sovereignty and integrity of India
- Has been expressly forbidden by court
- Causes breach of privilege of Parliament
- Relates to commercial confidence or trade secrets
- Is received in confidence from foreign government
- Endangers life or physical safety of any person
- Impedes investigation of crime
- Relates to personal information with no public interest

HOW TO FILE RTI APPLICATION
Step 1: Write application on plain paper in English, Hindi or local language
Step 2: Address to Public Information Officer (PIO) of concerned department
Step 3: Clearly state what information you want
Step 4: Pay Rs 10 fee by postal order, DD, cash or online
Step 5: Submit in person, by post, or online at rtionline.gov.in

TIME LIMITS FOR RESPONSE
Normal cases: 30 days from receipt of application
Life and liberty matters: 48 hours
Information from third party: 40 days
If PIO transfers to another PIO: 5 days extra

FIRST APPEAL
If not satisfied with response or no response received, appeal to First Appellate 
Authority within 30 days. FAA must decide within 30 days (extendable to 45 days).

SECOND APPEAL / COMPLAINT
Second appeal to Central Information Commission or State Information Commission 
within 90 days of FAA order. CIC/SIC has powers to impose penalty.

PENALTY FOR PIO
If PIO refuses to receive application, does not furnish information within time, 
knowingly gives incorrect or incomplete information: Rs 250 per day penalty 
up to maximum Rs 25,000. Also disciplinary action under service rules.

CENTRAL INFORMATION COMMISSION
Handles second appeals for central government departments. Chief Information 
Commissioner and up to 10 Information Commissioners appointed by President.

RTI AND PRIVATE BODIES
RTI applies to private bodies only if they are substantially funded by government 
or if they perform public functions.
"""

# NI Act - Cheque Bounce
ni_text = """
NEGOTIABLE INSTRUMENTS ACT 1881 - CHEQUE BOUNCE PROVISIONS

SECTION 138 - DISHONOUR OF CHEQUE FOR INSUFFICIENCY OF FUNDS
Where any cheque drawn by a person on an account maintained by him with a banker 
for payment of any amount of money to another person from out of that account 
for the discharge of any debt or other liability is returned by the bank unpaid 
either because of the amount of money standing to the credit of that account is 
insufficient to honour the cheque or that it exceeds the arrangement made by 
the drawer of the cheque with his banker, such person shall be deemed to have 
committed an offence.

PUNISHMENT: Imprisonment up to 2 years, or fine up to twice the cheque amount, or both.

CONDITIONS FOR OFFENCE UNDER SECTION 138
1. Cheque must be for discharge of legally enforceable debt
2. Cheque presented to bank within 3 months of issue date
3. Bank must return cheque unpaid
4. Payee must send demand notice within 30 days of cheque return memo
5. Drawer must fail to pay within 15 days of receiving notice
6. Complaint must be filed within 30 days of expiry of 15-day notice period

DEMAND NOTICE REQUIREMENTS
Notice must be in writing. Must demand payment of cheque amount. 
Must be sent within 30 days of receiving bank's return memo.
Send by registered post or speed post. Keep postal receipt safely.

SECTION 139 - PRESUMPTION IN FAVOUR OF HOLDER
It shall be presumed unless contrary is proved that the holder received the cheque 
for the discharge of a debt or other liability.

SECTION 140 - DEFENCE NOT ALLOWED
It shall not be a defence that the drawer had no reason to believe that the cheque 
would be dishonoured.

SECTION 141 - OFFENCES BY COMPANIES
If the person committing offence is a company, every person who at the time of 
offence was in charge of and responsible to the company for the conduct of business 
shall be deemed to be guilty of the offence.

SECTION 143 - POWER TO TRY CASES SUMMARILY
All cheque bounce offences shall be tried by Judicial Magistrate First Class.
Cases must be tried summarily for expedient disposal.

SECTION 143A - INTERIM COMPENSATION
Court may direct drawer to pay interim compensation up to 20% of cheque amount 
during pendency of trial.

SECTION 148 - POWER OF APPELLATE COURT
Appellate court may direct appellant to deposit minimum 20% of fine or compensation 
awarded while hearing appeal.

IMPORTANT JUDGMENTS
Dashrath Rupsingh Rathod vs State of Maharashtra (2014): Complaint must be filed 
where the cheque was presented to the bank.
M/s Meters and Instruments vs Kanchan Mehta (2017): Compounding of offence allowed 
even after conviction.
"""

